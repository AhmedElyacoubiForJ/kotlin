package basics

import kotlin.math.*

fun main() {
    val number1 = 25
    val number2 = 2

    println(kotlin.math.PI)
    println(PI)
    println("max($number1, $number2) = ${max(number1, number2)}")
    println("min($number1, $number2) = ${min(number1, number2)}")
    println("sqrt($number1) = ${sqrt(number1.toDouble())}")
    println("round(4.3) = ${round(4.3)}")
    println("round(4.5) = ${round(4.5)}")
    println("round(4.6) = ${round(4.6)}")
    println("round(4.7) = ${round(4.7)}")
}