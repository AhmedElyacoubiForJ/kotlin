package basics

fun main() {
    var number = 10

    println(++number) // output: 11, value: 11
    println(number++) // output: 11, value: 12
    println(number)   // output: 12, value: 12
    println()
    println(--number)   // output: 11, value: 11
    println(number--)   // output: 11, value: 10
    println(number)     // output: 10, value: 10


}